def insertion_sort(array)
  n = array.length
  # Если массив содержит 1 элемент или менее, он уже отсортирован
  return array if n <= 1
  # Проходим по массиву, начиная со второго элемента (индекс 1)
  (1...n).each do |i|
    # Текущий элемент, который нужно вставить на правильное место
    key = array[i]
    # Индекс предыдущего элемента, с которым будем сравнивать
    j = i - 1 
    # Пока индекс j находится в пределах массива и элемент с индексом j больше key
    while j >= 0 && array[j] > key
      # Перемещаем элемент вправо
      array[j + 1] = array[j]
      # Переходим к следующему предыдущему элементу
      j -= 1
    end
    # Вставляем key на правильное место
    array[j + 1] = key
  end
  # Возвращаем отсортированный массив
  array
end
